-- Manual schema changes required because spring.jpa.hibernate.ddl-auto=none.
-- Run as the application schema owner before starting the application.

ALTER TABLE item ADD version NUMBER DEFAULT 0 NOT NULL;

CREATE TABLE app_user (
    user_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(100) NOT NULL UNIQUE,
    password_hash VARCHAR2(255) NOT NULL,
    role VARCHAR2(30) NOT NULL,
    enabled NUMBER(1) DEFAULT 1 NOT NULL,
    employee_id NUMBER,
    CONSTRAINT fk_app_user_employee
        FOREIGN KEY (employee_id)
        REFERENCES employee(employee_id)
);

CREATE TABLE app_user_permission (
    user_id NUMBER NOT NULL,
    permission_code VARCHAR2(100) NOT NULL,
    CONSTRAINT pk_app_user_permission PRIMARY KEY (user_id, permission_code),
    CONSTRAINT fk_app_user_permission_user
        FOREIGN KEY (user_id) REFERENCES app_user(user_id) ON DELETE CASCADE
);

